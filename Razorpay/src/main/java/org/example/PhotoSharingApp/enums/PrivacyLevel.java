package org.example.PhotoSharingApp.enums;

/**
 * Enum representing privacy levels for photos.
 * Supports different visibility settings for shared photos.
 */
public enum PrivacyLevel {
    /**
     * Photo is visible to everyone
     */
    PUBLIC,
    
    /**
     * Photo is visible only to followers
     */
    FOLLOWERS_ONLY,
    
    /**
     * Photo is visible only to the owner
     */
    PRIVATE
}





